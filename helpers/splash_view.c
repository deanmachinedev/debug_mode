static void debug_mode_splash_view_draw(Canvas* canvas, void* model) {
    DebugModeSplashModel* splash = model;

    canvas_clear(canvas);
    canvas_set_color(canvas, ColorBlack);
    canvas_draw_xbm(canvas, 0, 0, 128, 48, debug_mode_splash_xbm);

    if(splash->phase % 2) {
        canvas_set_font(canvas, FontSecondary);
        canvas_draw_str_aligned(canvas, 64, 59, AlignCenter, AlignCenter, "CLICK TO START");
    }
}

static bool debug_mode_splash_view_input(InputEvent* event, void* context) {
    DebugMode* app = context;

    if((event->key == InputKeyOk) && (event->type == InputTypeShort)) {
        view_dispatcher_send_custom_event(app->view_dispatcher, DebugModeCustomEventSplashStart);
        return true;
    }

    return false;
}

View* debug_mode_splash_view_alloc(void* context) {
    View* view = view_alloc();
    view_allocate_model(view, ViewModelTypeLocking, sizeof(DebugModeSplashModel));
    view_set_context(view, context);
    view_set_draw_callback(view, debug_mode_splash_view_draw);
    view_set_input_callback(view, debug_mode_splash_view_input);

    with_view_model(
        view,
        DebugModeSplashModel * model,
        {
            model->phase = 1;
        },
        true);

    return view;
}

void debug_mode_splash_view_free(View* view) {
    view_free_model(view);
    view_free(view);
}

void debug_mode_splash_view_tick(View* view) {
    with_view_model(
        view,
        DebugModeSplashModel * model,
        {
            model->phase++;
        },
        true);
}
